package KruskalMaze;

class Edge {
    private final int x;
    private final int y;
    private final int direction;

    public Edge(int x, int y, int direction) {
        this.x = x;
        this.y = y;
        this.direction = direction;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getDirection() {
        return direction;
    }
}
